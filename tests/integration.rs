// ── dCBOR ─────────────────────────────────────────────────────────────────────

#[test]
fn test_dcbor_comment() {
    let mut opts = CodegenOptions::default();
    opts.dcbor = true;
    let g = compile_with_opts("s = { b: uint, a: tstr }", opts);
    assert!(g.source.contains("deterministic") || g.source.contains("dCBOR"));
}

#[test]
fn test_dcbor_sorts_keys() {
    let mut opts = CodegenOptions::default();
    opts.dcbor = true;
    let g = compile_with_opts("s = { b: uint, a: tstr }", opts);
    let a_pos = g.source.find("\"a\"").unwrap_or(usize::MAX);
    let b_pos = g.source.find("\"b\"").unwrap_or(0);
    assert!(a_pos < b_pos, "dCBOR: 'a' should be encoded before 'b'");
}

